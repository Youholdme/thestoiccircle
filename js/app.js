/* ── Language detection ── */
function detectLanguage() {
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get('lang');
  if (urlLang && translations[urlLang]) return urlLang;

  const stored = localStorage.getItem('stoic-lang');
  if (stored && translations[stored]) return stored;

  const browser = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
  if (translations[browser]) return browser;

  return 'en';
}

/* ── Apply translations ── */
function applyLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.documentElement.lang = lang;

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Attribute-based (title, description meta)
  if (t['title']) document.title = t['title'];
  const descMeta = document.querySelector('meta[name="description"]');
  if (descMeta && t['description']) descMeta.setAttribute('content', t['description']);

  // OG tags
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && t['hero.title1']) ogTitle.setAttribute('content', 'The Stoic Circle – ' + (t['hero.title2'] || ''));
  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc && t['description']) ogDesc.setAttribute('content', t['description']);

  // Update URL without reload
  const url = new URL(window.location);
  if (lang === 'en') {
    url.searchParams.delete('lang');
  } else {
    url.searchParams.set('lang', lang);
  }
  window.history.replaceState({}, '', url);

  // Store preference
  localStorage.setItem('stoic-lang', lang);

  // Update active states
  document.querySelectorAll('.lang-btn, .lang-btn-footer').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
    btn.setAttribute('aria-pressed', btn.dataset.lang === lang);
  });
}

/* ── Smooth scroll for anchors ── */
function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', e => {
      const target = document.querySelector(anchor.getAttribute('href'));
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

/* ── Intersection Observer for fade-in animations ── */
function initAnimations() {
  const style = document.createElement('style');
  style.textContent = `
    .animate-on-scroll { opacity: 0; transform: translateY(24px); transition: opacity 0.6s ease, transform 0.6s ease; }
    .animate-on-scroll.visible { opacity: 1; transform: translateY(0); }
  `;
  document.head.appendChild(style);

  document.querySelectorAll('.feature-card, .philosopher-card, .step').forEach((el, i) => {
    el.classList.add('animate-on-scroll');
    el.style.transitionDelay = `${(i % 3) * 0.1}s`;
  });

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.animate-on-scroll').forEach(el => observer.observe(el));
}

/* ── Nav scroll effect ── */
function initNavScroll() {
  const nav = document.querySelector('.nav');
  window.addEventListener('scroll', () => {
    nav.style.borderBottomColor = window.scrollY > 40
      ? 'rgba(139, 125, 94, 0.25)'
      : 'rgba(139, 125, 94, 0.15)';
  }, { passive: true });
}

/* ── Init ── */
document.addEventListener('DOMContentLoaded', () => {
  const lang = detectLanguage();
  applyLanguage(lang);

  // Lang buttons
  document.querySelectorAll('.lang-btn, .lang-btn-footer').forEach(btn => {
    btn.addEventListener('click', () => applyLanguage(btn.dataset.lang));
  });

  initSmoothScroll();
  initAnimations();
  initNavScroll();
});
